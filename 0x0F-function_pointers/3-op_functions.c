#include "3-calc.h"

/**
*op_add - adds 2 ints
*@a: int a
*@b: int b
*Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
*op_sub - subtracts 2 ints
*@a: int a
*@b: int b
*Return: difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
*op_mul - multiplies 2 ints
*@a: int a
*@b: int b
*Return: product
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
*op_div - divides 2 ints
*@a: int a
*@b: int b
*Return: quotient
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
*op_mod - mods 2 ints
*@a: int a
*@b: int b
*Return: remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
